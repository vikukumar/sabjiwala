const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const sharedDir = path.join(rootDir, 'shared');
const apps = ['customer-app', 'vendor-app', 'admin-app', 'delivery-app', 'sbjiwala-web', 'agent-app'];

function copyRecursiveSync(src, dest) {
  const exists = fs.existsSync(src);
  const stats = exists && fs.statSync(src);
  const isDirectory = exists && stats.isDirectory();
  if (isDirectory) {
    if (!fs.existsSync(dest)) {
      fs.mkdirSync(dest, { recursive: true });
    }
    fs.readdirSync(src).forEach((childItemName) => {
      copyRecursiveSync(path.join(src, childItemName), path.join(dest, childItemName));
    });
  } else {
    fs.copyFileSync(src, dest);
  }
}

function removeRecursiveSync(targetPath) {
  if (fs.existsSync(targetPath)) {
    fs.rmSync(targetPath, { recursive: true, force: true });
  }
}

apps.forEach((app) => {
  const appDir = path.join(rootDir, 'apps', app);
  if (!fs.existsSync(appDir)) return;

  console.log(`Fixing ${app} node_modules...`);
  const targetParent = path.join(appDir, 'node_modules', '@sbjiwala');
  if (!fs.existsSync(targetParent)) {
    fs.mkdirSync(targetParent, { recursive: true });
  }

  const targetPath = path.join(targetParent, 'shared');
  if (fs.existsSync(targetPath)) {
    console.log(`Removing old folder: ${targetPath}`);
    removeRecursiveSync(targetPath);
  }

  console.log(`Copying physical shared files to ${targetPath}...`);
  fs.mkdirSync(targetPath, { recursive: true });

  try {
    copyRecursiveSync(path.join(sharedDir, 'dist'), path.join(targetPath, 'dist'));
    copyRecursiveSync(path.join(sharedDir, 'src'), path.join(targetPath, 'src'));
    fs.copyFileSync(path.join(sharedDir, 'package.json'), path.join(targetPath, 'package.json'));
    fs.copyFileSync(path.join(sharedDir, 'tsconfig.json'), path.join(targetPath, 'tsconfig.json'));
    console.log(`Successfully copied to ${app}!`);
  } catch (err) {
    console.error(`Failed to copy to ${app}:`, err);
  }
});
